import sys # นำเข้าโมดูล sys เพื่อจัดการกับอินพุตและเอาต์พุต

sys.setrecursionlimit(200000) # เพิ่มขีดจำกัดการเรียกซ้ำของฟังก์ชันเพื่อรองรับกรณีที่มีโหนดจำนวนมากในกราฟ


def dfs(u, adj, visited): # ฟังก์ชัน DFS มาตรฐานที่ใช้ในการตรวจสอบความเชื่อมโยงของกราฟ
    visited.add(u) # ทำเครื่องหมายว่าโหนด u ถูกเยี่ยมชมแล้ว
    for v in adj[u]: # วนผ่านโหนดเพื่อนบ้านของ u
        if v not in visited: # ถ้าโหนด v ยังไม่ถูกเยี่ยมชม
            dfs(v, adj, visited) # เรียก DFS ซ้ำสำหรับโหนด v เพื่อเยี่ยมชมโหนดนั้นและเพื่อนบ้านของมันต่อไป


def solve():
    while True: # วนลูปเพื่ออ่านข้อมูลกราฟจากอินพุตจนกว่าจะเจอกรณีที่ไม่มีข้อมูล (N=0, M=0)
        line = sys.stdin.readline().strip() # อ่านบรรทัดจากอินพุตและตัดช่องว่างหัวท้ายออก
        if not line: # ถ้าไม่มีบรรทัดให้อ่าน (เช่น EOF) ให้หยุดการทำงานของลูป
            break

        N, M = map(int, line.split()) # แยกตัวเลข N และ M จากบรรทัดที่อ่านมาและแปลงเป็นจำนวนเต็ม

        if N == 0 and M == 0: # ถ้า N และ M เป็น 0 แสดงว่าไม่มีกราฟให้ประมวลผล ให้หยุดการทำงานของลูป
            break 

        adj = [[] for _ in range(N + 1)] # สร้างลิสต์ของลิสต์เพื่อเก็บกราฟในรูปแบบ adjacency list สำหรับกราฟปกติ
        adj_T = [[] for _ in range(N + 1)] # สร้างลิสต์ของลิสต์เพื่อเก็บกราฟในรูปแบบ adjacency list สำหรับกราฟกลับด้าน

        for _ in range(M): # วนลูป M ครั้งเพื่ออ่านข้อมูลของแต่ละขอบในกราฟ
            a, b, c = map(int, sys.stdin.readline().split()) # อ่านข้อมูลของขอบ (a, b) และประเภทของขอบ (c) จากอินพุต

            if c == 1: # ถ้า c เป็น 1 แสดงว่าขอบเป็นทิศทางเดียวจาก a ไป b
                adj[a].append(b) # เพิ่ม b เป็นเพื่อนบ้านของ a ในกราฟปกติ
                adj_T[b].append(a) # เพิ่ม a เป็นเพื่อนบ้านของ b ในกราฟกลับด้าน
            else:  # c == 2
                adj[a].append(b) # เพิ่ม b เป็นเพื่อนบ้านของ a ในกราฟปกติ
                adj[b].append(a) # เพิ่ม a เป็นเพื่อนบ้านของ b ในกราฟปกติ (เนื่องจากเป็นขอบสองทิศทาง)
                adj_T[a].append(b) # เพิ่ม b เป็นเพื่อนบ้านของ a ในกราฟกลับด้าน
                adj_T[b].append(a) # เพิ่ม a เป็นเพื่อนบ้านของ b ในกราฟกลับด้าน

        # DFS จาก node 1
        visited = set() # สร้างเซ็ตเพื่อเก็บโหนดที่ถูกเยี่ยมชม
        dfs(1, adj, visited) # เรียก DFS เพื่อเยี่ยมชมโหนดทั้งหมดที่เชื่อมต่อกับ node 1 ในกราฟปกติ

        if len(visited) != N: # ถ้าโหนดที่ถูกเยี่ยมชมไม่เท่ากับจำนวนโหนดทั้งหมด แสดงว่าไม่ใช่กราฟที่เชื่อมโยงกันอย่างสมบูรณ์
            print(0) # พิมพ์ 0 เพื่อแสดงว่าไม่ใช่กราฟที่เชื่อมโยงกันอย่างสมบูรณ์
            continue # ถ้าไม่ใช่กราฟที่เชื่อมโยงกันอย่างสมบูรณ์ ให้ข้ามไปประมวลผลกรณีถัดไป

        visited.clear() # ล้างเซ็ต visited เพื่อเตรียมสำหรับการตรวจสอบกราฟกลับด้าน
        dfs(1, adj_T, visited) # เรียก DFS เพื่อเยี่ยมชมโหนดทั้งหมดที่เชื่อมต่อกับ node 1 ในกราฟกลับด้าน

        if len(visited) != N: # ถ้าโหนดที่ถูกเยี่ยมชมในกราฟกลับด้านไม่เท่ากับจำนวนโหนดทั้งหมด แสดงว่าไม่ใช่กราฟที่เชื่อมโยงกันอย่างสมบูรณ์
            print(0) # พิมพ์ 0 เพื่อแสดงว่าไม่ใช่กราฟที่เชื่อมโยงกันอย่างสมบูรณ์
        else: # ถ้าโหนดที่ถูกเยี่ยมชมในกราฟกลับด้านเท่ากับจำนวนโหนดทั้งหมด แสดงว่าเป็นกราฟที่เชื่อมโยงกันอย่างสมบูรณ์
            print(1) # พิมพ์ 1 เพื่อแสดงว่าเป็นกราฟที่เชื่อมโยงกันอย่างสมบูรณ์


if __name__ == "__main__":
    input_filename = "lab9.txt"

    try:
        with open(input_filename, "r") as f:
            sys.stdin = f # เปลี่ยนแหล่งข้อมูลอินพุตเป็นไฟล์ที่กำหนด
            solve() # เรียกฟังก์ชัน solve เพื่อประมวลผลข้อมูลจากไฟล์และพิมพ์ผลลัพธ์
    except FileNotFoundError:
        print(f"ไม่พบไฟล์ {input_filename}") # แจ้งข้อผิดพลาดถ้าไม่พบไฟล์ที่กำหนด
