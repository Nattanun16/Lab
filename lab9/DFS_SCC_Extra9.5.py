from inspect import stack
import sys  # นำเข้าโมดูล sys เพื่อจัดการกับอินพุตและเอาต์พุต

sys.setrecursionlimit(
    200000
)  # เพิ่มขีดจำกัดการเรียกซ้ำของฟังก์ชันเพื่อรองรับกรณีที่มีโหนดจำนวนมากในกราฟ


def dfs(
    u, adj, visited, stack=None, comp=None
):  # ฟังก์ชัน DFS ที่รับพารามิเตอร์โหนดเริ่มต้น u, กราฟในรูปแบบ adjacency list adj, เซ็ต visited เพื่อเก็บโหนดที่ถูกเยี่ยมชม, stack สำหรับเก็บลำดับการเยี่ยมชม (ใช้ในกรณีของกราฟกลับด้าน), และ comp สำหรับเก็บโหนดที่อยู่ในคอมโพเนนต์เดียวกัน (ใช้ในกรณีของกราฟกลับด้าน)
    visited.add(u)  # ทำเครื่องหมายว่าโหนด u ถูกเยี่ยมชมแล้ว
    if comp is not None:  # ถ้า comp ไม่เป็น None แสดงว่าเรากำลังเก็บโหนดในคอมโพเนนต์เดียวกัน
        comp.append(u)  # เพิ่มโหนด u ลงในคอมโพเนนต์ปัจจุบัน
    for v in adj[u]:  # วนผ่านโหนดเพื่อนบ้านของ u
        if v not in visited:  # ถ้าโหนด v ยังไม่ถูกเยี่ยมชม
            dfs(
                v, adj, visited, stack, comp
            )  # เรียก DFS ซ้ำเพื่อเยี่ยมชมโหนด v และเพื่อนบ้านของมัน
    if stack is not None:  # ถ้า stack ไม่เป็น None แสดงว่าเรากำลังเก็บลำดับการเยี่ยมชม
        stack.append(u)  # เพิ่มโหนด u ลงใน stack หลังจากเยี่ยมชมเพื่อนบ้านทั้งหมด


def solve():
    while True:  # วนลูปเพื่ออ่านข้อมูลกราฟจากอินพุตจนกว่าจะเจอกรณีที่ไม่มีข้อมูล (N=0, M=0)
        line = sys.stdin.readline().strip()  # อ่านบรรทัดจากอินพุตและตัดช่องว่างหัวท้ายออก
        if not line:  # ถ้าไม่มีบรรทัดให้อ่าน (เช่น EOF) ให้หยุดการทำงานของลูป
            break

        N, M = map(
            int, line.split()
        )  # แยกตัวเลข N และ M จากบรรทัดที่อ่านมาและแปลงเป็นจำนวนเต็ม

        if (
            N == 0 and M == 0
        ):  # ถ้า N และ M เป็น 0 แสดงว่าไม่มีกราฟให้ประมวลผล ให้หยุดการทำงานของลูป
            break

        adj = [
            [] for _ in range(N + 1)
        ]  # สร้างลิสต์ของลิสต์เพื่อเก็บกราฟในรูปแบบ adjacency list สำหรับกราฟปกติ
        adj_T = [
            [] for _ in range(N + 1)
        ]  # สร้างลิสต์ของลิสต์เพื่อเก็บกราฟในรูปแบบ adjacency list สำหรับกราฟกลับด้าน

        for _ in range(M):  # วนลูป M ครั้งเพื่ออ่านข้อมูลของแต่ละขอบในกราฟ
            a, b, c = map(
                int, sys.stdin.readline().split()
            )  # อ่านข้อมูลของขอบ (a, b) และประเภทของขอบ (c) จากอินพุต

            if c == 1:  # ถ้า c เป็น 1 แสดงว่าขอบเป็นทิศทางเดียวจาก a ไป b
                adj[a].append(b)  # เพิ่ม b เป็นเพื่อนบ้านของ a ในกราฟปกติ
                adj_T[b].append(a)  # เพิ่ม a เป็นเพื่อนบ้านของ b ในกราฟกลับด้าน
            else:  # c == 2
                adj[a].append(b)  # เพิ่ม b เป็นเพื่อนบ้านของ a ในกราฟปกติ
                adj[b].append(
                    a
                )  # เพิ่ม a เป็นเพื่อนบ้านของ b ในกราฟปกติ (เนื่องจากเป็นขอบสองทิศทาง)
                adj_T[a].append(b)  # เพิ่ม b เป็นเพื่อนบ้านของ a ในกราฟกลับด้าน
                adj_T[b].append(a)  # เพิ่ม a เป็นเพื่อนบ้านของ b ในกราฟกลับด้าน

        # DFS จาก node 1
        visited = set()  # สร้างเซ็ตเพื่อเก็บโหนดที่ถูกเยี่ยมชม
        dfs(1, adj, visited)  # เรียก DFS เพื่อเยี่ยมชมโหนดทั้งหมดที่เชื่อมโยงกับ node 1 ในกราฟปกติ
        if (
            len(visited) == N
        ):  # ถ้าโหนดที่ถูกเยี่ยมชมมีจำนวนเท่ากับ N แสดงว่ากราฟเชื่อมโยงกันอย่างสมบูรณ์แล้ว
            visited.clear()  # ล้างเซ็ต visited เพื่อเตรียมสำหรับการตรวจสอบกราฟกลับด้าน
            dfs(
                1, adj_T, visited
            )  # เรียก DFS เพื่อเยี่ยมชมโหนดทั้งหมดที่เชื่อมโยงกับ node 1 ในกราฟกลับด้าน
            if (
                len(visited) == N
            ):  # ถ้าโหนดที่ถูกเยี่ยมชมมีจำนวนเท่ากับ N แสดงว่ากราฟเชื่อมโยงกันอย่างสมบูรณ์แล้ว
                print(1)  # พิมพ์ 1 เพื่อแสดงว่ากราฟเชื่อมโยงกันอย่างสมบูรณ์
                continue  # ข้ามไปยังกรณีถัดไป
        print(0)  # พิมพ์ 0 เพื่อแสดงว่ากราฟไม่เชื่อมโยงกันอย่างสมบูรณ์
        visited.clear()  # ล้างเซ็ต visited เพื่อเตรียมสำหรับการตรวจสอบกราฟกลับด้าน
        stack = []  # สร้างลิสต์เพื่อเก็บลำดับการเยี่ยมชมของโหนดในกราฟปกติ
        for i in range(1, N + 1):  # วนลูปผ่านโหนดทั้งหมดในกราฟ
            if i not in visited:  # ถ้าโหนด i ยังไม่ถูกเยี่ยมชม
                dfs(
                    i, adj, visited, stack
                )  # เรียก DFS เพื่อเยี่ยมชมโหนด i และเก็บลำดับการเยี่ยมชมใน stack
        visited.clear()  # ล้างเซ็ต visited เพื่อเตรียมสำหรับการตรวจสอบกราฟกลับด้าน
        scc_list = []  # สร้างลิสต์เพื่อเก็บคอมโพเนนต์ที่เชื่อมโยงกันอย่างสมบูรณ์ (SCC)
        while stack:  # วนลูปจนกว่า stack จะว่างเปล่า
            node = stack.pop()  # ดึงโหนดจาก stack เพื่อเริ่มต้นการเยี่ยมชมในกราฟกลับด้าน
            if node not in visited:  # ถ้าโหนดยังไม่ถูกเยี่ยมชม
                comp = []  # สร้างลิสต์เพื่อเก็บโหนดที่อยู่ในคอมโพเนนต์เดียวกัน
                dfs(
                    node, adj_T, visited, None, comp
                )  # เรียก DFS เพื่อเยี่ยมชมโหนดในกราฟกลับด้านและเก็บโหนดที่อยู่ในคอมโพเนนต์เดียวกันใน comp
                scc_list.append(comp)  # เพิ่มคอมโพเนนต์ที่เชื่อมโยงกันอย่างสมบูรณ์ลงใน scc_list
        print(
            f"SCC ทั้งหมด: {len(scc_list)}"
        )  # พิมพ์จำนวนคอมโพเนนต์ที่เชื่อมโยงกันอย่างสมบูรณ์ทั้งหมด
        for i, comp in enumerate(scc_list, 1): # วนลูปผ่านคอมโพเนนต์ที่เชื่อมโยงกันอย่างสมบูรณ์ทั้งหมดและพิมพ์โหนดในแต่ละคอมโพเนนต์
            print(f"Component {i}: {sorted(comp)}") # พิมพ์หมายเลขคอมโพเนนต์และโหนดที่อยู่ในคอมโพเนนต์นั้น โดยเรียงลำดับโหนดในคอมโพเนนต์

        print()  # เว้นบรรทัด


