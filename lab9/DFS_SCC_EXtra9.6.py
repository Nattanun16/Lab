from inspect import stack
import sys  # นำเข้าโมดูล sys เพื่อจัดการกับอินพุตและเอาต์พุต

sys.setrecursionlimit(
    200000
)  # เพิ่มขีดจำกัดการเรียกซ้ำของฟังก์ชันเพื่อรองรับกรณีที่มีโหนดจำนวนมากในกราฟ


def dfs(
    u, adj, visited, stack=None, comp=None
):  # ฟังก์ชัน DFS ที่รับพารามิเตอร์โหนดเริ่มต้น u, กราฟในรูปแบบ adjacency list adj, เซ็ต visited เพื่อเก็บโหนดที่ถูกเยี่ยมชม, stack สำหรับเก็บลำดับการเยี่ยมชม (ใช้ในกรณีของกราฟกลับด้าน), และ comp สำหรับเก็บโหนดที่อยู่ในคอมโพเนนต์เดียวกัน (ใช้ในกรณีของกราฟกลับด้าน)
    visited.add(u)  # ทำเครื่องหมายว่าโหนด u ถูกเยี่ยมชมแล้ว
    if comp is not None:  # ถ้า comp ไม่เป็น None แสดงว่าเรากำลังเก็บโหนดในคอมโพเนนต์เดียวกัน
        comp.append(u)  # เพิ่มโหนด u ลงในคอมโพเนนต์ปัจจุบัน
    for v in adj[u]:  # วนผ่านโหนดเพื่อนบ้านของ u
        if v not in visited:  # ถ้าโหนด v ยังไม่ถูกเยี่ยมชม
            dfs(
                v, adj, visited, stack, comp
            )  # เรียก DFS ซ้ำเพื่อเยี่ยมชมโหนด v และเพื่อนบ้านของมัน
    if stack is not None:  # ถ้า stack ไม่เป็น None แสดงว่าเรากำลังเก็บลำดับการเยี่ยมชม
        stack.append(u)  # เพิ่มโหนด u ลงใน stack หลังจากเยี่ยมชมเพื่อนบ้านทั้งหมด


def solve():
    while True:  # วนลูปเพื่ออ่านข้อมูลกราฟจากอินพุตจนกว่าจะเจอกรณีที่ไม่มีข้อมูล (N=0, M=0)
        line = sys.stdin.readline().strip()  # อ่านบรรทัดจากอินพุตและตัดช่องว่างหัวท้ายออก
        if not line:  # ถ้าไม่มีบรรทัดให้อ่าน (เช่น EOF) ให้หยุดการทำงานของลูป
            break

        N, M = map(
            int, line.split()
        )  # แยกตัวเลข N และ M จากบรรทัดที่อ่านมาและแปลงเป็นจำนวนเต็ม

        if (
            N == 0 and M == 0
        ):  # ถ้า N และ M เป็น 0 แสดงว่าไม่มีกราฟให้ประมวลผล ให้หยุดการทำงานของลูป
            break

        adj = [
            [] for _ in range(N + 1)
        ]  # สร้างลิสต์ของลิสต์เพื่อเก็บกราฟในรูปแบบ adjacency list สำหรับกราฟปกติ
        adj_T = [
            [] for _ in range(N + 1)
        ]  # สร้างลิสต์ของลิสต์เพื่อเก็บกราฟในรูปแบบ adjacency list สำหรับกราฟกลับด้าน

        for _ in range(M):  # วนลูป M ครั้งเพื่ออ่านข้อมูลของแต่ละขอบในกราฟ
            a, b, c = map(
                int, sys.stdin.readline().split()
            )  # อ่านข้อมูลของขอบ (a, b) และประเภทของขอบ (c) จากอินพุต

            if c == 1:  # ถ้า c เป็น 1 แสดงว่าขอบเป็นทิศทางเดียวจาก a ไป b
                adj[a].append(b)  # เพิ่ม b เป็นเพื่อนบ้านของ a ในกราฟปกติ
                adj_T[b].append(a)  # เพิ่ม a เป็นเพื่อนบ้านของ b ในกราฟกลับด้าน
            else:  # c == 2
                adj[a].append(b)  # เพิ่ม b เป็นเพื่อนบ้านของ a ในกราฟปกติ
                adj[b].append(
                    a
                )  # เพิ่ม a เป็นเพื่อนบ้านของ b ในกราฟปกติ (เนื่องจากเป็นขอบสองทิศทาง)
                adj_T[a].append(b)  # เพิ่ม b เป็นเพื่อนบ้านของ a ในกราฟกลับด้าน
                adj_T[b].append(a)  # เพิ่ม a เป็นเพื่อนบ้านของ b ในกราฟกลับด้าน

        # DFS จาก node 1
        visited = set()  # สร้างเซ็ตเพื่อเก็บโหนดที่ถูกเยี่ยมชม
        dfs(1, adj, visited)  # เรียก DFS เพื่อเยี่ยมชมโหนดทั้งหมดที่เชื่อมโยงกับ node 1 ในกราฟปกติ
        if (
            len(visited) == N
        ):  # ถ้าโหนดที่ถูกเยี่ยมชมมีจำนวนเท่ากับ N แสดงว่ากราฟเชื่อมโยงกันอย่างสมบูรณ์แล้ว
            visited.clear()  # ล้างเซ็ต visited เพื่อเตรียมสำหรับการตรวจสอบกราฟกลับด้าน
            dfs(
                1, adj_T, visited
            )  # เรียก DFS เพื่อเยี่ยมชมโหนดทั้งหมดที่เชื่อมโยงกับ node 1 ในกราฟกลับด้าน
            if (
                len(visited) == N
            ):  # ถ้าโหนดที่ถูกเยี่ยมชมมีจำนวนเท่ากับ N แสดงว่ากราฟเชื่อมโยงกันอย่างสมบูรณ์แล้ว
                print(1)  # พิมพ์ 1 เพื่อแสดงว่ากราฟเชื่อมโยงกันอย่างสมบูรณ์
                continue  # ข้ามไปยังกรณีถัดไป
        visited.clear()  # ล้างเซ็ต visited เพื่อเตรียมสำหรับการตรวจสอบกราฟกลับด้าน
        stack = []  # สร้างลิสต์เพื่อเก็บลำดับการเยี่ยมชมของโหนดในกราฟปกติ
        for i in range(1, N + 1):  # วนลูปผ่านโหนดทั้งหมดในกราฟ
            if i not in visited:  # ถ้าโหนด i ยังไม่ถูกเยี่ยมชม
                dfs(
                    i, adj, visited, stack
                )  # เรียก DFS เพื่อเยี่ยมชมโหนด i และเก็บลำดับการเยี่ยมชมใน stack
        visited.clear()  # ล้างเซ็ต visited เพื่อเตรียมสำหรับการตรวจสอบกราฟกลับด้าน
        scc_list = []  # สร้างลิสต์เพื่อเก็บคอมโพเนนต์ที่เชื่อมโยงกันอย่างสมบูรณ์ (SCC)
        while stack:  # วนลูปจนกว่า stack จะว่างเปล่า
            node = stack.pop()  # ดึงโหนดจาก stack เพื่อเริ่มต้นการเยี่ยมชมในกราฟกลับด้าน
            if node not in visited:  # ถ้าโหนดยังไม่ถูกเยี่ยมชม
                comp = []  # สร้างลิสต์เพื่อเก็บโหนดที่อยู่ในคอมโพเนนต์เดียวกัน
                dfs(
                    node, adj_T, visited, None, comp
                )  # เรียก DFS เพื่อเยี่ยมชมโหนดในกราฟกลับด้านและเก็บโหนดที่อยู่ในคอมโพเนนต์เดียวกันใน comp
                scc_list.append(comp)  # เพิ่มคอมโพเนนต์ที่เชื่อมโยงกันอย่างสมบูรณ์ลงใน scc_list
        for _ in range(len(scc_list)):  # วนลูปผ่านคอมโพเนนต์ที่เชื่อมโยงกันอย่างสมบูรณ์ทั้งหมด
            print(
                1
            )  # พิมพ์ 1 สำหรับแต่ละคอมโพเนนต์ที่เชื่อมโยงกันอย่างสมบูรณ์เพื่อแสดงว่ามีคอมโพเนนต์ที่เชื่อมโยงกันอย่างสมบูรณ์อยู่ในกราฟ
        print(
            f"SCC ทั้งหมด: {len(scc_list)}"
        )  # พิมพ์จำนวนคอมโพเนนต์ที่เชื่อมโยงกันอย่างสมบูรณ์ทั้งหมด
        for i, comp in enumerate(
            scc_list, 1
        ):  # วนลูปผ่านคอมโพเนนต์ที่เชื่อมโยงกันอย่างสมบูรณ์ทั้งหมดและพิมพ์โหนดในแต่ละคอมโพเนนต์
            print(
                f"Component {i}: {sorted(comp)}"
            )  # พิมพ์หมายเลขคอมโพเนนต์และโหนดที่อยู่ในคอมโพเนนต์นั้น โดยเรียงลำดับโหนดในคอมโพเนนต์

        print()  # เว้นบรรทัด

        scc_id = [0] * (
            N + 1
        )  # สร้างลิสต์เพื่อเก็บหมายเลขคอมโพเนนต์ที่เชื่อมโยงกันอย่างสมบูรณ์สำหรับแต่ละโหนด โดยเริ่มต้นเป็น 0 (ยังไม่กำหนดคอมโพเนนต์)
        for i, comp in enumerate(
            scc_list, 1
        ):  # วนลูปผ่านคอมโพเนนต์ที่เชื่อมโยงกันอย่างสมบูรณ์ทั้งหมดและกำหนดหมายเลขคอมโพเนนต์ให้กับโหนดในแต่ละคอมโพเนนต์
            for node in comp:  # วนลูปผ่านโหนดในคอมโพเนนต์ปัจจุบัน
                scc_id[node] = (
                    i  # กำหนดหมายเลขคอมโพเนนต์ให้กับโหนดในคอมโพเนนต์ปัจจุบัน โดยใช้หมายเลขคอมโพเนนต์ที่ได้จาก enumerate (เริ่มต้นที่ 1)
                )
        indeg = [0] * (
            len(scc_list) + 1
        )  # สร้างลิสต์เพื่อเก็บจำนวนขอบที่เข้ามาในแต่ละคอมโพเนนต์ที่เชื่อมโยงกันอย่างสมบูรณ์ โดยเริ่มต้นเป็น 0
        outdeg = [0] * (
            len(scc_list) + 1
        )  # สร้างลิสต์เพื่อเก็บจำนวนขอบที่ออกจากแต่ละคอมโพเนนต์ที่เชื่อมโยงกันอย่างสมบูรณ์ โดยเริ่มต้นเป็น 0
        for u in range(1, N + 1):  # วนลูปผ่านโหนดทั้งหมดในกราฟ
            for v in adj[u]:  # วนลูปผ่านโหนดเพื่อนบ้านของ u ในกราฟปกติ
                if (
                    scc_id[u] != scc_id[v]
                ):  # ถ้าโหนด u และ v อยู่ในคอมโพเนนต์ที่เชื่อมโยงกันอย่างสมบูรณ์ต่างกัน
                    outdeg[scc_id[u]] += 1  # เพิ่มจำนวนขอบที่ออกจากคอมโพเนนต์ของ u
                    indeg[scc_id[v]] += 1  # เพิ่มจำนวนขอบที่เข้ามาในคอมโพเนนต์ของ v
        sources = [
            i for i in range(1, len(scc_list) + 1) if indeg[i] == 0
        ]  # สร้างลิสต์ของคอมโพเนนต์ที่เชื่อมโยงกันอย่างสมบูรณ์ที่ไม่มีขอบเข้ามา (indeg เท่ากับ 0) ซึ่งเป็นคอมโพเนนต์ต้นทาง
        sinks = [
            i for i in range(1, len(scc_list) + 1) if outdeg[i] == 0
        ]  # สร้างลิสต์ของคอมโพเนนต์ที่เชื่อมโยงกันอย่างสมบูรณ์ที่ไม่มีขอบออกไป (outdeg เท่ากับ 0) ซึ่งเป็นคอมโพเนนต์ปลายทาง
        edges_needed = max(
            len(sources), len(sinks)
        )  # คำนวณจำนวนขอบที่ต้องเพิ่มเพื่อทำให้กราฟเชื่อมโยงกันอย่างสมบูรณ์ โดยใช้จำนวนคอมโพเนนต์ต้นทางและปลายทางที่มากที่สุดเป็นจำนวนขอบที่ต้องเพิ่ม
        print(
            f"ต้องเพิ่ม edge อย่างน้อย: {edges_needed}"
        )  # พิมพ์จำนวนขอบที่ต้องเพิ่มเพื่อทำให้กราฟเชื่อมโยงกันอย่างสมบูรณ์
        print("แนะนำให้เพิ่ม edge:")  # พิมพ์ข้อความแนะนำให้เพิ่มขอบเพื่อทำให้กราฟเชื่อมโยงกันอย่างสมบูรณ์
        k = edges_needed  # กำหนดตัวแปร k เป็นจำนวนขอบที่ต้องเพิ่มเพื่อทำให้กราฟเชื่อมโยงกันอย่างสมบูรณ์
        for i in range(edges_needed):  # วนลูปตามจำนวนขอบที่ต้องเพิ่มเพื่อแนะนำขอบที่ควรเพิ่ม
            u_comp = sinks[
                i % len(sinks)
            ]  # เลือกคอมโพเนนต์ปลายทางจากลิสต์ sinks โดยใช้การวนลูปแบบวงกลม (modulo) เพื่อให้แน่ใจว่าเราจะไม่เกินจำนวนคอมโพเนนต์ปลายทางที่มีอยู่
            v_comp = sources[
                (i + 1) % len(sources)
            ]  # เลือกคอมโพเนนต์ต้นทางจากลิสต์ sources โดยใช้การวนลูปแบบวงกลม (modulo) เพื่อให้แน่ใจว่าเราจะไม่เกินจำนวนคอมโพเนนต์ต้นทางที่มีอยู่
            u = scc_list[u_comp - 1][
                0
            ]  # เลือกโหนดตัวแทนจากคอมโพเนนต์ปลายทางที่เลือก (ใช้ u_comp - 1 เนื่องจาก scc_id เริ่มต้นที่ 1 แต่ดัชนีของ scc_list เริ่มต้นที่ 0)
            v = scc_list[v_comp - 1][
                0
            ]  # เลือกโหนดตัวแทนจากคอมโพเนนต์ต้นทางที่เลือก (ใช้ v_comp - 1 เนื่องจาก scc_id เริ่มต้นที่ 1 แต่ดัชนีของ scc_list เริ่มต้นที่ 0)
            print(
                f"{u} -> {v}"
            )  # พิมพ์คำแนะนำในการเพิ่มขอบจากโหนดตัวแทนของคอมโพเนนต์ปลายทางไปยังโหนดตัวแทนของคอมโพเนนต์ต้นทางเพื่อทำให้กราฟเชื่อมโยงกันอย่างสมบูรณ์


